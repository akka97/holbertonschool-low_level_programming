#include "3-calc.h"
/**
 * op_add - function that adds twp integers
 * @a: first integer
 * @b: second integer
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function that subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: diference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function that multiplicate two integers
 * @a: first integer
 * @b: second integer
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function that divides two integers
 * @a: first integer
 * @b: second integer
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function that finds the remainder of division
 * @a: first integer
 * @b: second integer
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

